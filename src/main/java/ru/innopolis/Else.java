package ru.innopolis;

public class Else {
    public static void main(String[] args) {
        int number = 7; // Создаем переменную типа int и присваиваем ей значение

        // Проверяем, четное ли число
        if (number % 2 == 0) {
            System.out.println("Четное число."); // Если число четное, выводим сообщение
        } else {
            System.out.println("Нечетное число."); // Если число нечетное, выводим другое сообщение
        }
    }
}

